fun main(){

    //for loop with range
    for (i in 1..10 ){
        println(i)
    }

    //for loop with until
    for (i in 1 until 10 ){
        println(i)
    }

    //iterate in reverse order
    for (i in 8 downTo 1){
        println(i)
    }

    //it will print nothing
    for (i in 5..1){
        println(i)
    }

    for (i in 10 downTo  2   step 2){
        println(i)
    }

}
